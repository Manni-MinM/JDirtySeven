public class ReverseCard extends Card {
	// Reverse Card : {10}

	// Constructor
	/**
	 * Creates ReverseCard Object
	 *
	 * @param rank Rank of the Card
	 * @param color Color of the Card
	 */
	public ReverseCard(String rank , String color) {
		this.rank = rank ;
		this.color = color ;
		this.point = 10 ;
	}
}

