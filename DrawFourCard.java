/**
 * A Class to hold information about the DrawFourCard
 *
 * @author Manni Moghimi
 * @version v1.0
 */
public class DrawFourCard extends Card {
	// Draw Four Cards : {7-BLACK}

	// Constructor
	/**
	 * Creates DrawFourCard Object
	 *
	 * @param rank Rank of the Card
	 * @param color Color of the Card
	 */
	public DrawFourCard(String rank , String color) {
		this.rank = rank ;
		this.color = color ;
		this.point = 15 ;
	}
}

